class User {
  String id;
  String firstName;
  String lastName;
  String email;
  String role;
  String avatar;
  String createdAt;
  String updatedAt;

  User({
    required this.id,
    required this.firstName,
    required this.lastName,
    required this.email,
    required this.role,
    required this.avatar,
    required this.createdAt,
    required this.updatedAt,
  });
}
